# Escreva um algoritmo para ler as dimensões de um retângulo (base e altura),
# calcular e escrever a área do retângulo

base = float(input("Informe a base do retângulo: "))
altura = float(input("Informe a altura do retângulo: "))
area_retangulo = base*altura

print(f"A área do retângulo é: {area_retangulo}m²")